package com.catDog.mpadoption;

public class Mpadoption {
	private long num;
	private String userId;
	private String nickName;
	
	private int adoptionListNum;
	private int adoptionNum;
	private int speciesSort;
	private String status;
	private String adoptionSubject;
	private String adoptionCreted;
	private int adoptionHitCount;


	public long getNum() {
		return num;
	}
	public void setNum(long num) {
		this.num = num;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}

	public int getAdoptionNum() {
		return adoptionNum;
	}
	public void setAdoptionNum(int adoptionNum) {
		this.adoptionNum = adoptionNum;
	}
	public int getSpeciesSort() {
		return speciesSort;
	}
	public void setSpeciesSort(int speciesSort) {
		this.speciesSort = speciesSort;
	}
	public String getAdoptionSubject() {
		return adoptionSubject;
	}
	public void setAdoptionSubject(String adoptionSubject) {
		this.adoptionSubject = adoptionSubject;
	}
	public String getAdoptionCreted() {
		return adoptionCreted;
	}
	public void setAdoptionCreted(String adoptionCreted) {
		this.adoptionCreted = adoptionCreted;
	}
	public int getAdoptionHitCount() {
		return adoptionHitCount;
	}
	public void setAdoptionHitCount(int adoptionHitCount) {
		this.adoptionHitCount = adoptionHitCount;
	}
	public int getAdoptionListNum() {
		return adoptionListNum;
	}
	public void setAdoptionListNum(int adoptionListNum) {
		this.adoptionListNum = adoptionListNum;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}

	
}
