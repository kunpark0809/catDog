package com.catDog.admin;

public class Money {
	private int smallSortNum;
	private String bigSortName, smallSortName;
	private String requestDate;
	private int productNum;
	private int productSum;

	public int getSmallSortNum() {
		return smallSortNum;
	}

	public void setSmallSortNum(int smallSortNum) {
		this.smallSortNum = smallSortNum;
	}

	public String getBigSortName() {
		return bigSortName;
	}

	public void setBigSortName(String bigSortName) {
		this.bigSortName = bigSortName;
	}

	public String getSmallSortName() {
		return smallSortName;
	}

	public void setSmallSortName(String smallSortName) {
		this.smallSortName = smallSortName;
	}

	public String getRequestDate() {
		return requestDate;
	}

	public void setRequestDate(String requestDate) {
		this.requestDate = requestDate;
	}

	public int getProductNum() {
		return productNum;
	}

	public void setProductNum(int productNum) {
		this.productNum = productNum;
	}

	public int getProductSum() {
		return productSum;
	}

	public void setProductSum(int productSum) {
		this.productSum = productSum;
	}

}
